    void habilitar() {
        txt_nombre_completo.setEnabled(true);
        txt_correo.setEnabled(true);
        txt_telefono.setEnabled(true);
        txt_direccion.setEnabled(true);

        btnguardar.setEnabled(true);
        btncancelar.setEnabled(true);
        txt_nombre_completo.setText("");
        txt_correo.setText("");
        txt_telefono.setText("");
        txt_direccion.setText("");
    }
    
    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        
        if (txt_nombre_completo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Nombre para el cliente");
            txt_nombre_completo.requestFocus();
            return;
        }
        if (txt_correo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido para el cliente");
            txt_correo.requestFocus();
            return;
        }
        
        if (txt_telefono.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un telefono para el cliente");
            txt_telefono.requestFocus();
            return;
        }
        

        if (txt_direccion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una direccion para el cliente");
            txt_direccion.requestFocus();
            return;
        }

        mclientes dts = new mclientes();
        cclientes func = new cclientes();

        dts.setNombre_cliente(txt_nombre_completo.getText());
        dts.setCorreo_cliente(txt_correo.getText());
        dts.setDireccion_cliente(txt_direccion.getText());
        dts.setTelefono_cliente(parseInt(txt_telefono.getText()));
        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "el cliente fue registrado satisfactoriamente");
                new vmenu().getContentPane().setVisible(true);
                dispose();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

